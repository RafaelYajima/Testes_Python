# Classificação de nota

num1 = int(input("Digite um numero entre 0-100:"))

if num1 <= 100 and num1 >= 90 :
    print("A")
elif num1 <= 90 and num1 >= 80 :
    print("B")
elif num1 <= 80 and num1 >= 70 :
    print("C")
elif num1 <= 70 and num1 >= 60 :
    print("D")
elif num1 <= 60 :
    print("F")



